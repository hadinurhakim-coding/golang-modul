package main

// Import package fmt untuk menampilkan pesan ke console
import "fmt"

func main() {
	// Menampilkan pesan "Hello, World!"
	fmt.Println("Hello, World!")

	// Menampilkan karakter pertama dari string "Halo, Nama saya Hadi Nurhakim"
	fmt.Println("Halo, Nama saya Hadi Nurhakim"[0])

	// Menampilkan panjang dari string "Halo, Nama saya Hadi Nurhakim"
	fmt.Println(len("Halo, Nama saya Hadi Nurhakim"))
}